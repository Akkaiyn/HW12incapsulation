public class City {
    private String name;
    private int vvp;
    private String[] regions;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getVvp() {
        return vvp;
    }

    public void setVvp(int vvp) {
        this.vvp = vvp;
    }

    public String[] getRegions() {
        return regions;
    }

    public void setRegions(String [] regions ) {
        this.regions = regions;
    }
}
